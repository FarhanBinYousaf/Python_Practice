#Multiplication 
print(2*3)
#Addition 
print(2+3)
#subtraction 
print(2-3)
#Power => 
print(2**3) 

#Floating Point Division 
print(4/2)
print(2/4)

# Integer Point Division 
print(4//2)
print(2//4)

# Square Root (Square root is actually 1/2 => 0.5)
print(4**0.5)

#Round Function => This will round off upto 4 digit coz i have given 4 as parameter
print(round(2**0.5,4))